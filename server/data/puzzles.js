/**
 * 获取指定主题的随机题目（5道）
 */
function getPuzzlesForTheme(theme) {
  const list = puzzles[theme];
  if (!list) return [];
  const shuffled = [...list].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(5, shuffled.length));
}

/**
 * 获取所有可用主题
 */
function getThemes() {
  return Object.keys(puzzles);
}

module.exports = { getPuzzlesForTheme, getThemes, puzzles };
